/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number}
 */
var minDepth = function (root) {
  const getDeep = (tree) => {
    if (!tree) {
      return 0;
    }
    if (!tree.left && !tree.right) {
      return 1;
    }
    if (!tree.left) {
      return getDeep(tree.right) + 1;
    }
    if (!tree.right) {
      return getDeep(tree.left) + 1;
    }
    return Math.min(getDeep(tree.left), getDeep(tree.right)) + 1
  }
  return getDeep(root)
};
