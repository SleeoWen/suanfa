/**
 * @param {number[][]} intervals
 * @return {number[][]}
 */
var merge = function (intervals) {
  intervals.sort((a, b) => {
    if (a[0] === b[0]) {
      return a[1] - b[1]
    }
    return a[0] - b[0]
  })
  let res = []
  let left = 0;
  let right = 0;
  const length = intervals.length;
  for (let i = 0; i < length; i++) {
    if (i === 0) {
      left = intervals[i][0];
      right = intervals[i][1];
    } else if (intervals[i][0] <= right) {
      right = Math.max(right, intervals[i][1]);
    } else {
      res.push([left, right]);
      left = intervals[i][0];
      right = intervals[i][1];
    }
    if (i === length - 1) {
      res.push([left, right]);
    }
  }
  return res;
};
