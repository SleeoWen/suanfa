/**
 * @param {TreeNode} root
 * @return {number}
 */
var sumOfLeftLeaves = function (root) {
  const getLeftLeaf = (node, isLeft) => {
    if (node === null) return 0;
    if (node.left === null && node.right === null && isLeft) return node.val;
    return getLeftLeaf(node.left, true) + getLeftLeaf(node.right, false);
  }
  return getLeftLeaf(root, false);
};
